    private void tambahSks(int sks) {
        ControllerMahasiswa crl = new ControllerMahasiswa(acc);
        Perwalian pw = crl.totalSks();
        int current_sks = pw.getSks();
        int totSks = current_sks + sks;
        crl.tambahSks(totSks);
    }

